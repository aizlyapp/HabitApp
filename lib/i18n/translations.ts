export type TranslationKey = keyof typeof translations;
export type SubKey<T extends TranslationKey> = keyof typeof translations[T];
export type Lang = 'es' | 'pt';

export function t(key: string, lang: Lang, params?: Record<string, string | number>): string {
  const keys = key.split('.');
  let value: unknown = translations;
  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = (value as Record<string, unknown>)[k];
    } else {
      console.warn(`Translation key not found: ${key}`);
      return key;
    }
  }
  if (value && typeof value === 'object' && lang in (value as Record<string, unknown>)) {
    let str = (value as Record<string, string>)[lang];
    if (params) {
      for (const [paramKey, paramValue] of Object.entries(params)) {
        str = str.replace(`{${paramKey}}`, String(paramValue));
      }
    }
    return str;
  }
  console.warn(`Translation not found: ${key}.${lang}`);
  return key;
}

export function getStoredLang(): Lang {
  if (typeof window === 'undefined') return 'es';
  const stored = localStorage.getItem('habitapp-lang');
  if (stored === 'es' || stored === 'pt') return stored;
  if (navigator.language.startsWith('pt')) return 'pt';
  return 'es';
}
